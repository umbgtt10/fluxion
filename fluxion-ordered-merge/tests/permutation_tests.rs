#[tokio::test]
async fn test_ordered_merge_permutations(
    #[case] channel_order: [usize; 3],
    #[case] send_order: [usize; 9],
) {
    // Test that temporal ordering (sequence numbers) is preserved
    // regardless of channel order or send pattern

    // Arrange - create three channels
    let person = FluxionChannel::new();
    let animal = FluxionChannel::new();
    let plant = FluxionChannel::new();

    // Build streams vec based on permutation order
    let streams = match channel_order {
        [0, 1, 2] => vec![person.stream, animal.stream, plant.stream],
        [0, 2, 1] => vec![person.stream, plant.stream, animal.stream],
        [1, 0, 2] => vec![animal.stream, person.stream, plant.stream],
        [1, 2, 0] => vec![animal.stream, plant.stream, person.stream],
        [2, 0, 1] => vec![plant.stream, person.stream, animal.stream],
        [2, 1, 0] => vec![plant.stream, animal.stream, person.stream],
        _ => panic!("Invalid permutation"),
    };

    let mut results = Box::pin(streams.ordered_merge());

    // Act - send values according to send_order
    // Track which value to send next for each channel
    let mut person_idx = 0;
    let mut animal_idx = 0;
    let mut plant_idx = 0;

    let mut expected_order = Vec::new();

    for &channel in &send_order {
        match channel {
            0 => {
                let value = match person_idx {
                    0 => person_alice(),
                    1 => person_bob(),
                    2 => person_charlie(),
                    _ => panic!("Too many person values"),
                };
                push(value.clone(), &person.sender);
                expected_order.push(value);
                person_idx += 1;
            }
            1 => {
                let value = match animal_idx {
                    0 => animal_dog(),
                    1 => animal_spider(),
                    2 => animal_cat(),
                    _ => panic!("Too many animal values"),
                };
                push(value.clone(), &animal.sender);
                expected_order.push(value);
                animal_idx += 1;
            }
            2 => {
                let value = match plant_idx {
                    0 => plant_rose(),
                    1 => plant_sunflower(),
                    2 => plant_fern(),
                    _ => panic!("Too many plant values"),
                };
                push(value.clone(), &plant.sender);
                expected_order.push(value);
                plant_idx += 1;
            }
            _ => panic!("Invalid channel index"),
        }
    }

    // Assert - all 9 values come out in send order
    assert_eq!(results.next().await.unwrap().value, expected_order[0]);
    assert_eq!(results.next().await.unwrap().value, expected_order[1]);
    assert_eq!(results.next().await.unwrap().value, expected_order[2]);
    assert_eq!(results.next().await.unwrap().value, expected_order[3]);
    assert_eq!(results.next().await.unwrap().value, expected_order[4]);
    assert_eq!(results.next().await.unwrap().value, expected_order[5]);
    assert_eq!(results.next().await.unwrap().value, expected_order[6]);
    assert_eq!(results.next().await.unwrap().value, expected_order[7]);
    assert_eq!(results.next().await.unwrap().value, expected_order[8]);
}
